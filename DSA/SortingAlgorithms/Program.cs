using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

using System.Threading.Tasks;

namespace DSA.SortingAlgorithms
{
	public class Program
	{
		
		static void PrintArray(int[] arr, int n)
		{
			for (int i = 0; i < n; i++)
			{
				Console.Write(arr[i] + " ");
			}
			Console.WriteLine();
		}
	}
}
